#include "testtest.h"

int		ft_segf03(void)
{
	size_t	i;
	size_t	n;
	void	*s;

	n = 2;
	i = 0;
	s = NULL;
	while (i != n + i)
	{
		*(unsigned char*)s++ = '\0';
		i++;
	}
	return (0);
}
