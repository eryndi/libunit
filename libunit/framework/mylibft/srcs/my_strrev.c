#include "libft_unit_tests.h"

char	*my_strrev(char *s)
{
	int		i;
	int		len;
	char	c;

	len = my_strlen(s);
	i = -1;
	while (++i < --len)
	{
		c = s[i];
		s[i] = s[len];
		s[len] = c;
	}
	return (s);
}
