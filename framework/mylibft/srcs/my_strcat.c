#include "libft_unit_tests.h"

char	*my_strcat(char *s1, const char *s2)
{
	int	i;
	int j;

	j = 0;
	i = my_strlen(s1);
	while (s2[j] != '\0')
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
